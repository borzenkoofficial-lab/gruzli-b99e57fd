export const LegalModal = ({ open, onClose, initialDoc }: LegalModalProps) => {
  const [activeDoc, setActiveDoc] = useState<DocType | null>(initialDoc || null);

  const handleClose = () => {
    setActiveDoc(null);
    onClose();
  };

  return (
    <AnimatePresence>
      {open && (
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          exit={{ opacity: 0 }}
          className="fixed inset-0 z-50 flex items-end sm:items-center justify-center p-4"
          onClick={handleClose}
        >
          <div className="absolute inset-0 bg-black/60 backdrop-blur-sm" />
          <motion.div
            initial={{ opacity: 0, y: 100 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: 100 }}
            transition={{ type: "spring", damping: 25, stiffness: 300 }}
            onClick={(e) => e.stopPropagation()}
            className="relative w-full max-w-md bg-card border border-border rounded-2xl z-10 overflow-hidden"
            style={{ maxHeight: "85vh" }}
          >
            <button onClick={handleClose} className="absolute top-4 right-4 z-10 text-muted-foreground">
              <X size={18} />
            </button>

            {activeDoc ? (
              <LegalDocumentViewer doc={activeDoc} onBack={() => setActiveDoc(null)} />
            ) : (
              <div className="p-5 space-y-3">
                <h3 className="text-base font-bold text-foreground pr-8">Юридическая информация</h3>
                <p className="text-xs text-muted-foreground">
                  Документы, регулирующие использование приложения Gruzli
                </p>
                {(Object.keys(docs) as DocType[]).map((key) => (
                  <button
                    key={key}
                    onClick={() => setActiveDoc(key)}
                    className="w-full text-left p-4 rounded-xl bg-muted/30 border border-border/50 hover:bg-muted/50 transition-colors"
                  >
                    <p className="text-sm font-semibold text-foreground">{docs[key].title}</p>
                    <p className="text-[11px] text-muted-foreground mt-0.5">Нажмите, чтобы прочитать</p>
                  </button>
                ))}
              </div>
            )}
          </motion.div>
        </motion.div>
      )}
    </AnimatePresence>
  );
};

